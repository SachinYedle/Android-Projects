public class RefugeData {
    private int id;
    private String name;
    private String itemid;

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setItemid(String itemid) {
        this.itemid = itemid;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getItemid() {
        return itemid;
    }
}

